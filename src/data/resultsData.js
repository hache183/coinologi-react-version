const sanitizeValue = (value = '') => value.replace(/\r/g, '').replace(/^"|"$/g, '').trim();

const parseItalianNumber = (value) => {
  const sanitized = sanitizeValue(value).replace(/\./g, '').replace(',', '.');
  const numeric = Number.parseFloat(sanitized);
  return Number.isNaN(numeric) ? null : numeric;
};

const parseMonthlyStats = (csv) => csv
  .trim()
  .split('\n')
  .slice(1)
  .map((line) => line.split(',').map(sanitizeValue))
  .filter((columns) => columns.length >= 6 && columns[0])
  .map(([month, totalTrades, takeProfit, stopLoss, successRate, totalProfit]) => ({
    month,
    totalTrades: parseInt(totalTrades, 10) || 0,
    takeProfit: parseInt(takeProfit, 10) || 0,
    stopLoss: parseInt(stopLoss, 10) || 0,
    successRate: parseItalianNumber(successRate),
    totalProfitPercent: parseItalianNumber(totalProfit)
  }));

const parseProfitValue = (value) => {
  const numeric = sanitizeValue(value).replace(/[^0-9,.-]/g, '').replace(',', '.');
  if (!numeric) {
    return null;
  }
  const parsed = Number.parseFloat(numeric);
  return Number.isNaN(parsed) ? null : parsed;
};

const parseTradeCsv = (csv, year) => csv
  .trim()
  .split('\n')
  .map((line) => line.trim())
  .filter((line) => line && !line.startsWith('Pair Name') && !line.startsWith(',,,,') && !line.startsWith('/*'))
  .map((line) => line.split(',').map(sanitizeValue))
  .map((columns) => {
    const [pair, openDate, closeDate, averageBuyPrice, lastPrice, totalProfit, status, ...rest] = columns;
    if (!pair) {
      return null;
    }

    return {
      year,
      pair,
      openDate: openDate || null,
      closeDate: closeDate || null,
      averageBuyPrice: averageBuyPrice || null,
      lastPrice: lastPrice || null,
      totalProfit: totalProfit || null,
      status: status || 'N/A',
      notes: rest.filter(Boolean).join(' ') || null,
      numericProfit: parseProfitValue(totalProfit)
    };
  })
  .filter(Boolean);

